function convert(qq) {
  return { text: qq.q, points: 10, answers: qq.a.map((text, i) => ({ text, is_correct: i === qq.c })) }
}
function poolFor(poolMap, grade) {
  const keys = Object.keys(poolMap)
  for (const k of keys) {
    const items = poolMap[k]
    const filtered = items.filter(qq => !qq.g || qq.g.includes(grade))
    if (filtered.length >= 5) return filtered
  }
  return []
}
function getPool(allPools, grade) {
  if (!allPools) return []
  const arr = Array.isArray(allPools) ? allPools : [allPools]
  let result = []
  for (const p of arr) {
    result = result.concat(poolFor(p, grade))
  }
  return result
}

