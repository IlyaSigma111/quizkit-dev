function shuffle(a) {
  for (let i = a.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]]
  }
  return a
}
function pick(arr, n) {
  const cp = [...arr]; shuffle(cp); return cp.slice(0, n)
}
function randomInt(m, M) { return m + Math.floor(Math.random() * (M - m + 1)) }
