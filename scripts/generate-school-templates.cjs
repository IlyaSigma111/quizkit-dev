const GRADE_EMOJI = { 5: '5️⃣', 6: '6️⃣', 7: '7️⃣', 8: '8️⃣', 9: '9️⃣', 10: '🔟', 11: '1️⃣1️⃣' }

const SUBJECT_EMOJI = {
  'Математика': '', 'Алгебра': '', 'Геометрия': '',
  'Русский язык': '', 'Литература': '',
  'История': '', 'Обществознание': '', 'Биология': '', 'География': '',
  'Физика': '', 'Химия': '', 'Английский язык': '', 'Информатика': '',
}

function tagFor(gradeIdx, topicIdx, totalTopics) {
  const quarterPos = (topicIdx / totalTopics)
  if (quarterPos < 0.25) return 'Проверочная работа'
  if (quarterPos < 0.5) return 'Викторина'
  if (quarterPos < 0.75) return 'Проверочная работа'
  return 'Правда и Ложь'
}

function quizTitle(grade, subject, topic) {
  return `${GRADE_EMOJI[grade]} ${grade} класс: ${topic}`
}

function quizDesc(grade, subject, topic, count) {
  const se = SUBJECT_EMOJI[subject] || ''
  return `${se} ${subject}: ${count} вопросов по теме «${topic}»`
}

const templates = []
let idCounter = 1

for (let grade = 5; grade <= 11; grade++) {
  const subjects = CURRICULUM[grade]
  if (!subjects) continue
  for (const [subject, info] of Object.entries(subjects)) {
    const allQuestions = getPool(info.pool, grade)
    if (allQuestions.length < 5) continue
    const topics = info.topics
    const totalTopics = topics.length
    topics.forEach((topic, idx) => {
      const tag = tagFor(grade, idx, totalTopics)
      let count = tag === 'Правда и Ложь' ? randomInt(5, 7) : randomInt(5, 8)
      const questions = pick(allQuestions, count).map(convert)
      if (tag === 'Правда и Ложь') {
        questions.forEach(qq => {
          qq.answers = [{ text: 'Верно', is_correct: true }, { text: 'Неверно', is_correct: false }]
          qq.text = 'Верно ли утверждение: ' + qq.text
        })
      }
      const totalTime = questions.length * (tag === 'Правда и Ложь' ? 15 : randomInt(30, 60))
      const id = `school-${grade}-${subject.toLowerCase().replace(/[^a-zа-яё]/g, '')}-${String(idCounter).padStart(3, '0')}`
      templates.push({
        id,
        title: quizTitle(grade, subject, topic),
        description: quizDesc(grade, subject, topic, questions.length),
        emoji: GRADE_EMOJI[grade],
        tag,
        category: `${subject} ${grade} класс`,
        grade,
        subject,
        total_time_seconds: totalTime,
        questions,
      })
      idCounter++
    })
  }
}

// Merge with existing templates.json
const existingPath = path.join(__dirname, '..', 'public', 'templates.json')
let existing = []
try {
  if (fs.existsSync(existingPath)) {
    const raw = fs.readFileSync(existingPath, 'utf-8')
    const parsed = JSON.parse(raw)
    if (parsed.templates) existing = parsed.templates
  }
} catch (e) {
  console.log('No existing templates.json, creating new')
}

const merged = [...existing, ...templates]
const output = JSON.stringify({ templates: merged }, null, 2)
fs.writeFileSync(existingPath, output, 'utf-8')
console.log(`Generated ${templates.length} school templates (merged: ${merged.length} total) → public/templates.json`)
